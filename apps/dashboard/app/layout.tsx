import { COOKIE_ACCESS_TOKEN } from '@/config/constants';
import { AuthProvider } from '@/modules/auth/providers/auth-provider';
import { AppChrome } from '@/modules/common/components/app-chrome';
import { ThemeFavicon } from '@/modules/common/components/brand/theme-favicon';
import { QueryProvider } from '@/providers/query-provider';
import { ThemeProvider } from '@/providers/theme-provider';
import { DashboardTooltipProvider } from '@/providers/tooltip-provider';
import '@repo/ui/index.css';
import { beVietnamPro } from '@repo/ui/lib/fonts';
import type { Metadata } from 'next';
import { cookies } from 'next/headers';
import 'react-quill-new/dist/quill.snow.css';
import { Toaster } from 'sonner';

export const metadata: Metadata = {
  title: 'Dashboard',
  description: 'Generated by create turbo',
  icons: {
    icon: '/images/favicon-light.png',
  },
};

export default async function RootLayout({ children }: { children: React.ReactNode }) {
  const cookieStore = await cookies();
  const accessToken = cookieStore.get(COOKIE_ACCESS_TOKEN)?.value ?? null;

  return (
    <html lang="vi" className={beVietnamPro.className} suppressHydrationWarning>
      <body>
        <ThemeProvider>
          <ThemeFavicon />
          <DashboardTooltipProvider>
            <QueryProvider>
              <AuthProvider accessToken={accessToken}>
                <AppChrome>{children}</AppChrome>
                <Toaster richColors closeButton position="top-center" />
              </AuthProvider>
            </QueryProvider>
          </DashboardTooltipProvider>
        </ThemeProvider>
      </body>
    </html>
  );
}
