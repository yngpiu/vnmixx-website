import { AppChrome } from '@/components/app-chrome';
import { COOKIE_ACCESS_TOKEN } from '@/lib/constants';
import { AuthProvider } from '@/providers/auth-provider';
import { QueryProvider } from '@/providers/query-provider';
import { DashboardTooltipProvider } from '@/providers/tooltip-provider';
import '@repo/ui/globals.css';
import { inter } from '@repo/ui/lib/fonts';
import type { Metadata } from 'next';
import { cookies } from 'next/headers';
import { Toaster } from 'sonner';

export const metadata: Metadata = {
  title: 'VNMIXX - Dashboard',
  description: 'Generated by create turbo',
  icons: {
    icon: [
      { url: '/images/favicon-light.png', media: '(prefers-color-scheme: light)' },
      { url: '/images/favicon-dark.png', media: '(prefers-color-scheme: dark)' },
    ],
  },
};

export default async function RootLayout({ children }: { children: React.ReactNode }) {
  const cookieStore = await cookies();
  const accessToken = cookieStore.get(COOKIE_ACCESS_TOKEN)?.value ?? null;

  return (
    <html lang="en" suppressHydrationWarning>
      <body className={inter.className}>
        <DashboardTooltipProvider>
          <QueryProvider>
            <AuthProvider accessToken={accessToken}>
              <AppChrome>{children}</AppChrome>
              <Toaster richColors closeButton position="top-center" />
            </AuthProvider>
          </QueryProvider>
        </DashboardTooltipProvider>
      </body>
    </html>
  );
}
